# Python program to find the perimeter of a rectangle
# 1. Take length and breadth of rectangle
# 2. Calculate perimeter of rectangle
# 3. Display calculated perimeter on screen
length = 5
breadth = 15
print("length is", length)
print("breadth is", breadth)
# Calculate the perimeter = 2 * (length + breadth)
perimeter = 2 * (length + breadth)

print("Perimeter of rectangle is", perimeter)
